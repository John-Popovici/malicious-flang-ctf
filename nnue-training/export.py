"""
Export trained NNUE model to various formats for integration with C/C++.

Exports model weights and biases to a binary format that can be loaded
by the C engine.

Supports quantization:
- Weights: 8-bit integers (int8)
- Accumulators: 32-bit integers (int32)
- Uses per-layer quantization scales
"""

import torch
import argparse
import struct
import numpy as np
from pathlib import Path
from model import FlangNNUE


# Quantization constants (int16 throughout for better precision)
# SCALE: quantization scale for input layer weights/accumulators
# QA: quantization scale for hidden layer activations (after SCReLU)
# QB: quantization scale for subsequent layer weights
SCALE = 700
QA = 255
QB = 256  # All layers use int16 with higher scale for better precision


def quantize_weights(weights, scale, use_int16=False):
    """
    Quantize float32 weights to int8 or int16.

    Args:
        weights: numpy array of float weights
        scale: quantization scale factor
        use_int16: if True, use int16 instead of int8

    Returns:
        quantized_weights: int8 or int16 array
        actual_scale: actual scale used (may differ slightly)
    """
    # Scale weights
    scaled = weights * scale

    if use_int16:
        # Clamp to int16 range [-32767, 32767] (avoiding -32768 for symmetry)
        clamped = np.clip(scaled, -32767, 32767)
        quantized = np.round(clamped).astype(np.int16)
    else:
        # Clamp to int8 range [-127, 127] (avoiding -128 for symmetry)
        clamped = np.clip(scaled, -127, 127)
        quantized = np.round(clamped).astype(np.int8)

    return quantized, scale


def quantize_biases(biases, scale):
    """
    Quantize float32 biases to int32.

    For the input layer, biases are scaled by SCALE.
    For other layers, biases are scaled by QA*QB.

    Args:
        biases: numpy array of float biases
        scale: quantization scale factor

    Returns:
        quantized_biases: int32 array
    """
    scaled = biases * scale
    quantized = np.round(scaled).astype(np.int32)
    return quantized


def export_to_binary(model, output_file):
    """
    Export model to binary format.

    Binary format:
    - Header: magic number (4 bytes), version (4 bytes)
    - Layer count (4 bytes)
    - For each layer:
      - Input size (4 bytes)
      - Output size (4 bytes)
      - Weights (floats)
      - Biases (floats)
    """
    with open(output_file, 'wb') as f:
        # Write header
        magic = 0x4E4E5545  # "NNUE" in hex
        version = 1
        f.write(struct.pack('II', magic, version))

        # Extract linear layers
        linear_layers = [m for m in model.network if isinstance(m, torch.nn.Linear)]

        # Write layer count
        f.write(struct.pack('I', len(linear_layers)))

        # Write each layer
        for layer in linear_layers:
            weight = layer.weight.detach().cpu().numpy()
            bias = layer.bias.detach().cpu().numpy()

            input_size, output_size = weight.shape[1], weight.shape[0]

            # Write dimensions
            f.write(struct.pack('II', input_size, output_size))

            # Write weights (row-major order)
            for w in weight.flatten():
                f.write(struct.pack('f', w))

            # Write biases
            for b in bias.flatten():
                f.write(struct.pack('f', b))

    print(f"Model exported to binary format: {output_file}")
    print(f"File size: {Path(output_file).stat().st_size / 1024:.2f} KB")


def export_to_c_header(model, output_file):
    """
    Export model to C header file format.

    This creates arrays that can be compiled directly into C code.
    """
    with open(output_file, 'w') as f:
        f.write("// Auto-generated Flang NNUE model\n")
        f.write("// DO NOT EDIT THIS FILE MANUALLY\n\n")
        f.write("#ifndef FLANG_NNUE_H\n")
        f.write("#define FLANG_NNUE_H\n\n")

        # Extract linear layers
        linear_layers = [m for m in model.network if isinstance(m, torch.nn.Linear)]

        # Write layer dimensions
        f.write(f"#define NNUE_NUM_LAYERS {len(linear_layers)}\n\n")

        for i, layer in enumerate(linear_layers):
            weight = layer.weight.detach().cpu().numpy()
            bias = layer.bias.detach().cpu().numpy()

            input_size, output_size = weight.shape[1], weight.shape[0]

            f.write(f"#define LAYER_{i}_INPUT_SIZE {input_size}\n")
            f.write(f"#define LAYER_{i}_OUTPUT_SIZE {output_size}\n\n")

            # Write weights
            f.write(f"static const float layer_{i}_weights[{output_size}][{input_size}] = {{\n")
            for row in weight:
                f.write("    {")
                f.write(", ".join(f"{v:.8f}f" for v in row))
                f.write("},\n")
            f.write("};\n\n")

            # Write biases
            f.write(f"static const float layer_{i}_biases[{output_size}] = {{\n")
            f.write("    ")
            f.write(", ".join(f"{v:.8f}f" for v in bias))
            f.write("\n};\n\n")

        f.write("#endif // FLANG_NNUE_H\n")

    print(f"Model exported to C header: {output_file}")
    print(f"File size: {Path(output_file).stat().st_size / 1024:.2f} KB")


def export_to_c_header_quantized(model, output_file):
    """
    Export model to C header file format with quantization.

    Weights are quantized to int8, biases to int32.
    Uses standard NNUE quantization scheme:
    - Layer 0 (input): weights scaled by SCALE, biases by SCALE
    - Layer 1+: weights scaled by QB, biases by QA*QB

    This creates arrays that can be compiled directly into C code.
    """
    with open(output_file, 'w') as f:
        f.write("// Auto-generated Flang NNUE model (QUANTIZED)\n")
        f.write("// DO NOT EDIT THIS FILE MANUALLY\n\n")
        f.write("#ifndef FLANG_NNUE_MODEL_H\n")
        f.write("#define FLANG_NNUE_MODEL_H\n\n")
        f.write("#include <stdint.h>\n\n")

        # Write quantization constants
        f.write("// Quantization constants\n")
        f.write(f"#define NNUE_SCALE {SCALE}\n")
        f.write(f"#define NNUE_QA {QA}\n")
        f.write(f"#define NNUE_QB {QB}\n\n")

        # Extract linear layers
        linear_layers = [m for m in model.network if isinstance(m, torch.nn.Linear)]

        # Write layer dimensions
        f.write(f"#define NNUE_NUM_LAYERS {len(linear_layers)}\n\n")

        for i, layer in enumerate(linear_layers):
            weight = layer.weight.detach().cpu().numpy()
            bias = layer.bias.detach().cpu().numpy()

            input_size, output_size = weight.shape[1], weight.shape[0]

            f.write(f"#define LAYER_{i}_INPUT_SIZE {input_size}\n")
            f.write(f"#define LAYER_{i}_OUTPUT_SIZE {output_size}\n\n")

            # Quantize weights and biases (all int16)
            if i == 0:
                # Input layer: scale by SCALE, use int8 (quantized range fits within [-127, 127])
                q_weights, _ = quantize_weights(weight, SCALE, use_int16=False)
                q_biases = quantize_biases(bias, SCALE)
            elif i == len(linear_layers) - 1:
                # Output layer: use int16 for higher precision
                q_weights, _ = quantize_weights(weight, QB, use_int16=True)
                q_biases = quantize_biases(bias, QA * QB)
            else:
                # Hidden layers: use int8
                q_weights, _ = quantize_weights(weight, QB, use_int16=False)
                q_biases = quantize_biases(bias, QA * QB)

            # Write quantized weights
            # Layer 0 is transposed to [input_size][output_size] for cache-friendly accumulator updates
            weight_type = "int16_t" if i == len(linear_layers) - 1 else "int8_t"
            if i == 0:
                q_weights_out = q_weights.T  # [output][input] -> [input][output]
                f.write(f"static const {weight_type} layer_{i}_weights[{input_size}][{output_size}] = {{\n")
            else:
                q_weights_out = q_weights
                f.write(f"static const {weight_type} layer_{i}_weights[{output_size}][{input_size}] = {{\n")
            for row in q_weights_out:
                f.write("    {")
                f.write(", ".join(f"{int(v)}" for v in row))
                f.write("},\n")
            f.write("};\n\n")

            # Write quantized biases (int32)
            f.write(f"static const int32_t layer_{i}_biases[{output_size}] = {{\n")
            f.write("    ")
            f.write(", ".join(f"{int(v)}" for v in q_biases))
            f.write("\n};\n\n")

        f.write("#endif // FLANG_NNUE_MODEL_H\n")

    print(f"Quantized model exported to C header: {output_file}")
    print(f"File size: {Path(output_file).stat().st_size / 1024:.2f} KB")

    # Print quantization statistics
    print("\nQuantization statistics:")
    for i, layer in enumerate(linear_layers):
        weight = layer.weight.detach().cpu().numpy()

        is_output = i == len(linear_layers) - 1
        scale = SCALE if i == 0 else QB
        q_weights, _ = quantize_weights(weight, scale, use_int16=is_output)
        dtype = "int16" if is_output else "int8"

        print(f"  Layer {i}:")
        print(f"    Weight range: [{weight.min():.6f}, {weight.max():.6f}]")
        print(f"    Quantized range: [{q_weights.min()}, {q_weights.max()}]")
        print(f"    Scale: {scale}, dtype: {dtype}")


def main():
    parser = argparse.ArgumentParser(description='Export Flang NNUE model')
    parser.add_argument('--model', type=str, required=True,
                        help='Path to trained model (.pt file)')
    parser.add_argument('--format', type=str, choices=['binary', 'c_header', 'both'],
                        default='both', help='Export format')
    parser.add_argument('--output', type=str, default='exported_model',
                        help='Output filename (without extension)')
    parser.add_argument('--quantize', action='store_true',
                        help='Export with quantization (int8 weights, int32 accumulators)')

    args = parser.parse_args()

    # Load model
    print(f"Loading model from: {args.model}")
    checkpoint = torch.load(args.model, map_location='cpu')

    # Create model with same architecture
    hidden_sizes = checkpoint.get('hidden_sizes', [128, 32])
    model = FlangNNUE(hidden_sizes=hidden_sizes)
    model.load_state_dict(checkpoint['model_state_dict'])
    model.eval()

    print(f"Model loaded successfully")
    print(f"  Epoch: {checkpoint.get('epoch', 'unknown')}")
    print(f"  Val Loss: {checkpoint.get('val_loss', 'unknown'):.6f}")
    print(f"  Hidden sizes: {hidden_sizes}")
    print(f"  Quantization: {'enabled' if args.quantize else 'disabled'}")

    # Export
    if args.format in ['binary', 'both']:
        export_to_binary(model, f"{args.output}.nnue")

    if args.format in ['c_header', 'both']:
        if args.quantize:
            export_to_c_header_quantized(model, f"{args.output}.h")
        else:
            export_to_c_header(model, f"{args.output}.h")

    print("\nExport complete!")


if __name__ == "__main__":
    main()